'''
data = 7 , data2 = 5를 입력하고
+, -, *, / 등등의 사칙연산을 출력하시오
'''
data = 7
data2 = 5
print("%d + %d = %d"%(data, data2, data + data2))
print("%d - %d = %d"%(data, data2, data - data2))
print("%d * %d = %d"%(data, data2, data * data2))
print("%d / %d = %d"%(data, data2, data / data2))



'''선생님풀이'''
