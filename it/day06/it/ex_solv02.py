#입력한 데이터가 3의 배수인경우 출력하시오
print("Hello world!")

in1 = int(input("숫자 입력 : "))

if in1 % 3 == 0 :
    print("입력하신 숫자는 3의 배수입니다!")
else :
    print("입력하신 숫자가 3의 배수가 아닙니다.")



