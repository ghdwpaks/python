# 어떤 진수를 10진수로 변환해서 볼 수 있다.
print(0b100)
print(0o100)
print(0x100)
# 어떤 진수를 2진수로 변환해서 볼 수 있다.
print("===============================")
print(bin(0o100))
print(bin(100))
print(bin(0x100))
# 어떤 진수를 16진수로 변환해서 볼 수 있다.
print("===============================")
print(hex(0b100))
print(hex(0o100))
print(hex(100))